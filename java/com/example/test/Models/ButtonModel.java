package com.example.test.Models;

public class ButtonModel {
    String name, borderHexCode, focusHexCode, placeParam;
    int iconID;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBorderHexCode() {
        return borderHexCode;
    }

    public void setBorderHexCode(String borderHexCode) {
        this.borderHexCode = borderHexCode;
    }

    public String getFocusHexCode() {
        return focusHexCode;
    }

    public void setFocusHexCode(String focusHexCode) {
        this.focusHexCode = focusHexCode;
    }

    public String getPlaceParam() {
        return placeParam;
    }

    public void setPlaceParam(String placeParam) {
        this.placeParam = placeParam;
    }

    public int getIconID() {
        return iconID;
    }

    public void setIconID(int iconID) {
        this.iconID = iconID;
    }

}
